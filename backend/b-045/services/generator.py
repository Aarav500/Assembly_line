def _yaml_escape(value: str) -> str:
    if value is None:
        return "''"
    v = str(value)
    if any(c in v for c in [":", "-", "#", "\n", "\"", "'"]) or v.strip() != v:
        v = v.replace("\n", "\\n").replace("\"", "\\\"")
        return f'"{v}"'
    return v


def _parse_dependencies(dep_str: str):
    deps = []
    if dep_str:
        for raw in dep_str.replace("\n", ",").split(","):
            d = raw.strip()
            if d:
                deps.append(d)
    return deps


def _parse_env_vars(env_text: str):
    envs = []
    if not env_text:
        return envs
    for line in env_text.splitlines():
        line = line.strip()
        if not line or line.startswith("#"):
            continue
        if "=" in line:
            key, val = line.split("=", 1)
            envs.append((key.strip(), val.strip()))
        else:
            envs.append((line, ""))
    return envs


def _manifest_yaml(ctx):
    services = ctx.get("services") or {}
    now = datetime.utcnow().isoformat() + "Z"
    lines = [
        f"schemaVersion: 1",
        f"name: {_yaml_escape(ctx['project_name'])}",
        f"slug: {_yaml_escape(ctx['slug'])}",
        f"description: {_yaml_escape(ctx['description'])}",
        f"author: {_yaml_escape(ctx['author'])}",
        f"license: {_yaml_escape(ctx['license'])}",
        f"language: python",
        f"framework: flask",
        f"python: {_yaml_escape(ctx['python_version'])}",
        f"ci: {_yaml_escape(ctx['ci_provider'])}",
        f"createdAt: {_yaml_escape(now)}",
        f"includeDocker: {str(bool(ctx['include_docker'])).lower()}",
        f"includeTests: {str(bool(ctx['include_tests'])).lower()}",
        f"services:",
        f"  postgres: {str(bool(services.get('postgres'))).lower()}",
        f"  redis: {str(bool(services.get('redis'))).lower()}",
        f"dependencies:",
    ]
    for d in _parse_dependencies(ctx.get("dependencies") or ""):
        lines.append(f"  - {_yaml_escape(d)}")
    envs = _parse_env_vars(ctx.get("env_vars") or "")
    if envs:
        lines.append("env:")
        for k, v in envs:
            safe_v = v if v else ""  # do not leak secrets
            lines.append(f"  {k}: {_yaml_escape(safe_v)}")
    return "\n".join(lines) + "\n"


def _readme_md(ctx):
    services = ctx.get("services") or {}
    deps = _parse_dependencies(ctx.get("dependencies") or "")
    envs = _parse_env_vars(ctx.get("env_vars") or "")
    ci = ctx.get("ci_provider")
    lines = [
        f"# {ctx['project_name']}",
        "",
        ctx.get("description") or "A Flask project generated by the Idea-to-Project wizard.",
        "",
        "## Stack",
        "- Python",
        "- Flask",
        f"- CI: {ci}",
    ]
    if services.get("postgres"):
        lines.append("- PostgreSQL")
    if services.get("redis"):
        lines.append("- Redis")
    lines += [
        "",
        "## Getting Started",
        "- Create and activate a virtual environment",
        "- Install dependencies: `pip install -r requirements.txt`",
    ]
    if ctx.get("include_tests"):
        lines.append("- Dev deps: `pip install -r requirements-dev.txt`")
    lines += [
        "- Run app: `make run` (or `python run.py`)",
        "",
        "## Environment",
        "Copy `.env.example` to `.env` and fill values.",
        "",
        "## Makefile targets",
        "- install, install-dev, run, lint, test",
        "",
        "## Generated",
        "- manifest.yaml",
        "- CI workflow",
        "- Dockerfile and docker-compose (if selected)",
    ]
    if deps:
        lines += ["", "## Declared dependencies"]
        for d in deps:
            lines.append(f"- {d}")
    if envs:
        lines += ["", "## Environment variables"]
        for k, _ in envs:
            lines.append(f"- {k}")
    return "\n".join(lines) + "\n"


